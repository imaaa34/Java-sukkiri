package chapter5;

public class IntroduceOneself {
	public static void main(String[] args) {
		introduceOneself();
	}
	public static void introduceOneself() {
		String name = "Scotty";
		int age = 24;
		double height = 178.5;
		char sex = '男';
		System.out.println("私の名前は" + name + "です。年齢は" + age + "歳で身長は" + height + "です。性別は" + sex + "です。");
	}
}
